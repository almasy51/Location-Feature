import UIKit

extension UIColor {
    
    class var gold: UIColor {
        return UIColor(hex: 0xafa626)
    }
    
    class var darkerGray: UIColor {
        return UIColor(hex: 0x6d6d6d)
    }
    
    class var lightBrown: UIColor {
        return UIColor(hex: 0xb28e2c)
    }
    
    class var teal: UIColor {
        return UIColor(hex: 0x34afa9)
    }
    
    class var darkBlue: UIColor {
        return UIColor(hex: 0x2a6991)
    }
}
